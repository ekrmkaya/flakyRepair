def run_plus_syntax(pair, repo_path, out_dir, env, timeout, log_path):
    """Run reproduction using Surefire PLUS_SYNTAX + reversealphabetical runOrder.

    No file generation needed — surefire:test runs the existing compiled classes.
    Polluter class must sort alphabetically AFTER victim class for this to work.
    """
    package       = pair["package"]
    victim_cls    = pair["victim_class"].split(".")[-1]
    victim_meth   = pair["victim_method"]
    polluter_cls  = pair["polluter_class"].split(".")[-1]
    polluter_meth = pair["polluter_method"]
    offline       = offline_flag_for(pair["repo_slug"])

    pl = f"-pl '{package}'" if package not in (".", "") else ""
    test_spec = f"{polluter_cls}#{polluter_meth},{victim_cls}#{victim_meth}"
    mvn = pair.get("mvn_cmd", "mvn")
    cmd = (f"{mvn} surefire:test {pl} -B {offline} "
           f"-Dtest=\"{test_spec}\" "
           f"-DforkCount=1 -DreuseForks=true "
           f"-Dsurefire.runOrder=reversealphabetical "
           f"-Dsurefire.failIfNoSpecifiedTests=false "
           f"{SKIP_FLAGS}")

    return run_cmd(cmd, repo_path, env, timeout, log_path)


def run_undertow(pair, repo_path, out_dir, env, timeout, log_path):
    """Write two generated files, compile+run with PLUS_SYNTAX, then clean up.

    NOT GENERALIZABLE — see module docstring.

    Mirrors old pipeline's section4b_programmatic_repro.py:
      1. UndertowRunner_{key}.java — extends DefaultServer, sorts polluter first
      2. UndertowSuite_{key}.java  — extends AnnotatedEndpointTest, @RunWith(Runner)
    Run via PLUS_SYNTAX so surefire executes polluter then victim in order.
    """
    safe_key      = pair["row_key"].replace("-", "_")
    package       = pair["package"]          # "websockets-jsr"
    victim_class  = pair["victim_class"]     # fully-qualified class name
    victim_meth   = pair["victim_method"]
    polluter_meth = pair["polluter_method"]
    mvn           = pair.get("mvn_cmd", "mvn")

    # Place generated files in flakyrepro/ under the module's test source tree
    flakyrepro_dir = os.path.join(repo_path, package, "src", "test", "java", "flakyrepro")
    os.makedirs(flakyrepro_dir, exist_ok=True)

    runner_name = f"UndertowRunner_{safe_key}"
    suite_name  = f"UndertowSuite_{safe_key}"
    runner_path = os.path.join(flakyrepro_dir, f"{runner_name}.java")
    suite_path  = os.path.join(flakyrepro_dir, f"{suite_name}.java")

    with open(runner_path, "w", encoding="utf-8") as f:
        f.write(_UNDERTOW_RUNNER_TEMPLATE.format(
            SAFE_KEY=safe_key,
            POLLUTER_METHOD=polluter_meth,
            VICTIM_METHOD=victim_meth,
        ))
    with open(suite_path, "w", encoding="utf-8") as f:
        f.write(_UNDERTOW_SUITE_TEMPLATE.format(
            SAFE_KEY=safe_key,
            VICTIM_CLASS=victim_class,
        ))

    # Two-step approach to avoid intermittent file-write timeouts in undertow:
    #   Step 1: test-compile (compiles the two flakyrepro files + main sources).
    #           Resources copy is skipped (already in target from step2).
    #           Annotation processing is disabled (-proc:none) so the JBoss
    #           logging processor doesn't try to write .i18n.properties files.
    #           Retry once if a file-write timeout occurs.
    #   Step 2: surefire:test directly — no lifecycle, no recompile.
    compile_flags = (f"-Dmaven.resources.skip=true "
                     f"-Dmaven.compiler.compilerArgument=-proc:none "
                     f"{SKIP_FLAGS}")
    compile_cmd = f"{mvn} -pl '{package}' -B -q {compile_flags} test-compile"
    compile_log  = log_path + ".compile"

    rc_c, out_c = run_cmd(compile_cmd, repo_path, env, timeout, compile_log)

    # Retry compile once if it hit a file-write timeout
    if rc_c != 0 and "Operation timed out" in out_c:
        rc_c, out_c = run_cmd(compile_cmd, repo_path, env, timeout, compile_log)

    test_spec = f"flakyrepro.{suite_name}#{polluter_meth}+{victim_meth}"
    run_surefire = (f"{mvn} surefire:test -pl '{package}' -B "
                    f"-Dsurefire.runOrder=alphabetical "
                    f"-Dtest=\"{test_spec}\" "
                    f"-DforkCount=1 -DreuseForks=true "
                    f"-Dsurefire.failIfNoSpecifiedTests=false "
                    f"-Dsurefire.useFile=false "
                    f"{SKIP_FLAGS}")

    rc, output = run_cmd(run_surefire, repo_path, env, timeout, log_path)

    # Merge compile log into main log
    compile_out = ""
    if os.path.isfile(compile_log):
        with open(compile_log) as f:
            compile_out = f.read()
        _cleanup(compile_log)
    with open(log_path, "r+") as f:
        body = f.read()
        f.seek(0)
        f.write(f"=== COMPILE (rc={rc_c}) ===\n{compile_out}\n=== SUREFIRE ===\n{body}")

    _cleanup(runner_path)
    _cleanup(suite_path)
    return rc, output


# ── Result parsing ─────────────────────────────────────────────────────────────

def parse_repro_result(output, victim_method, polluter_method):
    """
    Classify the outcome of a polluter+victim run.

    Returns one of:
      REPRODUCED
      NOT_REPRODUCED:VICTIM_PASSES_WITH_POLLUTER
      NOT_REPRODUCED:POLLUTER_ALSO_FAILED
      NOT_REPRODUCED:UNKNOWN
      BUILD_ERROR
      TESTS_NOT_FOUND
      TIMEOUT
    """
    if "TIMEOUT after" in output:
        return "TIMEOUT"

    if "No tests were run" in output or "No tests matching" in output:
        return "TESTS_NOT_FOUND"

    if not re.search(r"Tests run:", output):
        return "BUILD_ERROR"

    victim_failed = bool(
        re.search(rf"{re.escape(victim_method)}.*FAILURE", output) or
        re.search(rf"FAILURE.*{re.escape(victim_method)}", output) or
        re.search(rf"{re.escape(victim_method)}.*<<<\s*FAILURE", output) or
        "[REPRODUCED]" in output
    )
    polluter_failed = bool(
        re.search(rf"{re.escape(polluter_method)}.*FAILURE", output) or
        re.search(rf"FAILURE.*{re.escape(polluter_method)}", output)
    )

    if victim_failed and not polluter_failed:
        return "REPRODUCED"
    if not victim_failed:
        return "NOT_REPRODUCED:VICTIM_PASSES_WITH_POLLUTER"
    if polluter_failed:
        return "NOT_REPRODUCED:POLLUTER_ALSO_FAILED"
    return "NOT_REPRODUCED:UNKNOWN"


def parse_victim_alone_result(output, victim_method):
    """
    Classify the result of running the victim in isolation.

    Returns: "PASSES_ALONE" | "FAILS_ALONE" | "BUILD_ERROR" | "TESTS_NOT_FOUND"
    """
    if not re.search(r"Tests run:", output):
        return "BUILD_ERROR"
    if "No tests were run" in output:
        return "TESTS_NOT_FOUND"

    victim_failed = bool(
        re.search(rf"{re.escape(victim_method)}.*FAILURE", output) or
        re.search(rf"FAILURE.*{re.escape(victim_method)}", output) or
        re.search(rf"Tests run:.*Failures: [1-9]", output) or
        re.search(rf"Tests run:.*Errors: [1-9]", output)
    )
    return "FAILS_ALONE" if victim_failed else "PASSES_ALONE"
